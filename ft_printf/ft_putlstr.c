#include "ft_printf.h"

int	ft_putlstr(char *s)
{
	int	i;

	i = -1;
	if (!s)
		return (write(1, "(null)", 6));
	while (s[++i] != '\0')
		write(1, &s[i], 1);
	return (i);
}
